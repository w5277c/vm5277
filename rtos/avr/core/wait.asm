.include "./core/wait_ms.inc"

.IFNDEF C5_WAIT
;--------------------------------------------------------
C5_WAIT:
;--------------------------------------------------------
;Ждем 5ms
;--------------------------------------------------------
	PUSH ACCUM_H
	PUSH ACCUM_L

	LDI ACCUM_H,0x00
	LDI ACCUM_L,0x05
	MCALL WAIT_MS

	POP ACCUM_L
	POP ACCUM_H
	RET
.ENDIF

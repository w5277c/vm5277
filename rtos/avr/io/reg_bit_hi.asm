;Включаем бит по в ячейке расположенной по адресу 0-255
;-----------------------------------------------------------------------------------------------------------------------
.IFNDEF REG_BIT_HI
;-----------------------------------------------------------
REG_BIT_HI:
;-----------------------------------------------------------
;Включаем бит регистра
;IN: Z-адрес,ACCUM_L-число бита
;-----------------------------------------------------------
	PUSH_T16

	LDS TEMP_H,SREG
	CLI
	LD TEMP_L,Z
	OR TEMP_L,ACCUM_L
	ST Z,TEMP_L
	STS SREG,TEMP_H

	POP_T16
	RET
.ENDIF

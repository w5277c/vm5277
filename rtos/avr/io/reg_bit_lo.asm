;Выключаем бит по в ячейке расположенной по адресу 0-255
;-----------------------------------------------------------------------------------------------------------------------
.IFNDEF REG_BIT_LO
;--------------------------------------------------------
REG_BIT_LO:
;--------------------------------------------------------
;Выключаем бит регистра
;IN: Z-адрес,ACCUM_L-число бита
;--------------------------------------------------------
	PUSH_T16
	PUSH ACCUM_L

	COM ACCUM_L
	LDS TEMP_H,SREG
	CLI

	LD TEMP_L,Z
	AND TEMP_L,ACCUM_L
	ST Z,TEMP_L

	STS SREG,TEMP_H

	POP ACCUM_L
	POP_T16
	RET
.ENDIF

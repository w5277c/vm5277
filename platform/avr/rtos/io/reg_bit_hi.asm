;--------------------------------------------------------
REG_BIT_HI:
;--------------------------------------------------------
;Включаем бит регистра
;IN: TEMP-адрес,ACCUM_L-число бита
;--------------------------------------------------------
	PUSH_Z
	MOV ZH,C0x00
	MOV ZL,TEMP
	RCALL REG_BIT_HI_BY_Z
	POP_Z
	RET

REG_BIT_HI_BY_Z:
	PUSH_T16
	LDS TEMP_H,SREG
	CLI
	LD TEMP_L,Z
	OR TEMP_L,ACCUM_L
	ST Z,TEMP_L
	STS SREG,TEMP_H
	POP_T16
	RET
